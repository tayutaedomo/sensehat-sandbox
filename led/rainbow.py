if __name__ == '__main__':
    sense = SenseHat()
    rainbows = [
        LedRaindow(sense),
        LedRaindowT2B(sense),
        LedRaindowB2T(sense),
        LedRaindowL2R(sense),
        LedRaindowR2L(sense),
        LedRaindowLT2RB(sense),
        LedRaindowLB2RT(sense),
        LedRaindowRT2LB(sense),
        LedRaindowRB2LT(sense),
    ]
    r_idx = 0
    rainbow = rainbows[r_idx]

    try:
        chFlg = 0
        prev_direction = None
        interval = 0

        while chFlg >= 0:
            for event in sense.stick.get_events():
                if event.direction == prev_direction:
                    prev_direction = None
                    continue

                if event.direction == "up":
                    chFlg = 1
                    rainbow.reset()

                if event.direction == "left":
                    interval += 1

                if event.direction == "down":
                    chFlg = 0

                if event.direction == "right":
                    #interval -= 1
                    #interval = 0 if interval < 0 else interval
                    r_idx += 1
                    r_idx = 0 if r_idx == len(rainbows) else r_idx
                    rainbow = rainbows[r_idx]
                    rainbow.reset()

                if event.direction == "middle":
                    chFlg = -1
                    sense.clear()

                prev_direction = event.direction

            if chFlg == 1:
                rainbow.next()
                msleep(interval)

    except KeyboardInterrupt:
        sense.clear()
        sys.exit()

